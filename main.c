#include "ft_ls.h"

int		main(int ac, char **av)
{
	if (ac == 1 || (ft_strcmp(av[1], "--") == 0))
		ft_ls_no_arg(NULL, NULL);
	else if (ac > 1)
		ft_get_arg(ac, av);
	return (0);
}
